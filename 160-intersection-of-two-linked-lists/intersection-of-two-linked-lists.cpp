/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *getIntersectionNode(ListNode *headA, ListNode *headB) {
        ListNode*tempA=headA;
        ListNode*tempB=headB;
        int leA=0;
        int lenB=0;
        while(tempA!=NULL){
            leA++;
            tempA=tempA->next;
        }
         while(tempB!=NULL){
            lenB++;
            tempB=tempB->next;
        }
        tempA=headA;
        tempB=headB;
        if(leA>lenB){
            int skip=leA-lenB;
            for(int i=1;i<=skip;i++){
                tempA=tempA->next;
            }
            while(tempA!=tempB){
                tempA=tempA->next;
                tempB=tempB->next;
            }
           return tempA;
        }
         else{
            int skip=lenB-leA;
             for(int i=1;i<=skip;i++){
                tempB=tempB->next;
            }
            while(tempA!=tempB){
                tempA=tempA->next;
                tempB=tempB->next;
            }
           return tempA;
        }

    }
};